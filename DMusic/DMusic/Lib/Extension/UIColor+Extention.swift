import Foundation
import UIKit

extension UIColor {
    // #F5F5F5
    static var whiteSmoke: UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1)
    }
    // #808080
    static var grayColor: UIColor {
        return UIColor(red: 128/255, green: 128/255, blue: 128/255, alpha: 1)
    }
}
